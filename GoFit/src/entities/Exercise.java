package entities;

public class Exercise {
	private String name;
	private String details;
	
	public String getName() {
		return name;
	}
	
	public String getDetails() {
		return details;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setDetails(String details) {
		this.details = details;
	}
}
